/*
2. Create a program that uses increment and decrement operators on a variable and prints the result each time.
*/

#include <iostream>
using namespace std;

int main() {
    int i = 0;
    cout << "i: " << i << endl;
    cout << "i++: " << i++ << endl;
    cout << "i: " << i << endl;
    cout << "++i: " << ++i << endl;
    cout << "i: " << i << endl;
    cout << "i--: " << i-- << endl;
    cout << "i: " << i << endl;
    cout << "--i: " << --i << endl;
    cout << "i: " << i << endl;
    return 0;
}
